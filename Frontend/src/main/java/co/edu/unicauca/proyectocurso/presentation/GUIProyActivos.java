    private void cargarProyectos() {
        List<Project> proyectos = projectService.getInExecutionProjects();
        model.setRowCount(0);
        for (Project p : proyectos) {
            model.addRow(new Object[]{p.getName(), p.getDescription(), p.getCompany().getName()});
        }
    }

